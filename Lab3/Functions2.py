def abSingle(n):
    return n["imdb"] > 5.5


def abGlobal(n):
    return list(filter(abSingle, n))


def category(n, cat):
    return list(filter(lambda x: x["category"] == cat, n))


def avgIMDB(n):
    avg = 0
    for i in n:
        avg += i["imdb"]
    return avg / len(n)


def avgcatIMDB(n, cat):
    n = category(n, cat)
    return avgIMDB(n)


